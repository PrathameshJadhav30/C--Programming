#include <iostream>
#include <cmath>
using namespace std;
int main() {
    
  // Initialization of different parameters    
  double x = 3.14;
  cout << "floor of " << x << " is " << floor(x) << endl;

  x = -3.14;
  cout << "floor of " << x << " is " << floor(x) << endl;

  x = 3.9;
  cout << "floor of " << x << " is " << floor(x) << endl;

  x = -3.9;
  cout << "floor of " << x << " is " << floor(x) << endl;

  x = 3.0;
  cout << "floor of " << x << " is " << floor(x) << endl;

  x = -3.0;
  cout << "floor of " << x << " is " << floor(x) << endl;

  return 0;
}