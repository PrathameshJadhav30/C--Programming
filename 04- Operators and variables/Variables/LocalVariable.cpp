#include <stdio.h> // Include the necessary header for standard I/O functions

int main() {
    int a, b, c; // Declare three integer variables a, b, and c

    // Initialize the variables
    a = 10;  // Assign value 10 to variable a
    b = 30;  // Assign value 30 to variable b

    // Perform the addition and store the result in c
    c = a + b; // c stores the sum of a and b

    // Print the result of the addition
    printf("%d\n", c); // Print the value of c (which is 40)

    return 0; // Exit the program successfully
}
