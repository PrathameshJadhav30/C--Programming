#include <stdio.h> // Include the necessary header for standard I/O functions

int main() {
    int a = 25, b = 4, c = 67; // Declare and initialize three integer variables

    // Print the values of the variables 'a', 'b', and 'c'
    printf("Value of a: %d\n", a); // Print the value of 'a'
    printf("Value of b: %d\n", b); // Print the value of 'b'
    printf("Value of c: %d\n", c); // Print the value of 'c'

    return 0; // Exit the program successfully
}
