#include <iostream> // Include the necessary header for I/O operations
using namespace std;

int main() {
    // Declaration and initialization of variables
    int var = 10;       // Integer variable initialized to 10
    float var2 = 10.25; // Floating-point variable initialized to 10.25

    // Output the value of var (integer)
    cout << "Value of var: " << var << endl;

    // Output the value of var2 (floating-point)
    cout << "Value of var2: " << var2 << endl;

    return 0; // Exit the program successfully
}
