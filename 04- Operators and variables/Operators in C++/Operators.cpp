#include <iostream>
using namespace std;

int main() {
    int a = 20, b = 3;

    // Output the sum of a and b
    cout << "a + b = " << (a + b) << endl;

    // Output the difference between a and b
    cout << "a - b = " << (a - b) << endl;

    // Output the product of a and b
    cout << "a * b = " << (a * b) << endl;

    // Output the division of a by b
    // Since both a and b are integers, the result is also an integer
    cout << "a / b = " << (a / b) << endl;

    // Output the remainder when a is divided by b
    cout << "a % b = " << (a % b) << endl;

    return 0;
}
